// Build-time selector for the "evento in evidenza" spotlight on /eventi.
//
// This is a static export with no CI/cron rebuild pipeline in this repo (no
// GitHub Actions configured), so a build-time-only selection can go stale
// between deploys: if nobody rebuilds the site, the HTML keeps showing
// whatever was current on the last build date, including a now-past event.
// To keep this reliable without a rebuild schedule, the fix runs at two
// levels:
//
//   1. Build-time (this script): computes the correct featured event using
//      the real clock at build time (new Date()) and bakes it into the
//      static HTML that ships to crawlers, social previews and browsers
//      without JS. This remains the source of truth and the only thing
//      that matters for SEO/AEO/GEO and no-JS readability.
//   2. Runtime reconciliation (js/featured-event-runtime.js, progressive
//      enhancement only): a small deterministic script re-runs the exact
//      same pickFeaturedEvent() rule against a manifest of dates that is
//      embedded inline in the page (no fetch, no dependencies). If the
//      visitor's clock disagrees with what was baked in at the last build
//      (i.e. the static build has gone stale), it swaps in the correct
//      pre-rendered alternate that was already generated by this same
//      script - never invented client-side, never a different rendering
//      path. If build-time and runtime agree (the common case right after
//      a deploy), nothing changes in the DOM and there is no flicker.
//
// There is no random rotation and no hardcoded "today" in either layer.
//
// Rule: pick the first event, in chronological order by startDate, whose
// window [startDate, endDate] contains "today" (an event "in corso"), or -
// if none is in progress - the first future event by startDate. If no
// current or future event exists, render an editorial fallback (links to
// the events calendar and the summer 2026 hub) without inventing any date.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { FEATURED_EVENT_CANDIDATES } from "./featured-events-data.mjs";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const root = resolve(scriptDir, "..");
const HUB_FILE = "eventi.html";
const MARKER_START = "<!-- FEATURED_EVENT:START -->";
const MARKER_END = "<!-- FEATURED_EVENT:END -->";

function findEventNode(node) {
  if (Array.isArray(node)) {
    for (const item of node) {
      const found = findEventNode(item);
      if (found) return found;
    }
    return null;
  }
  if (node && typeof node === "object") {
    if (node["@type"] === "Event" || node["@type"] === "MusicEvent") {
      return node;
    }
    for (const value of Object.values(node)) {
      const found = findEventNode(value);
      if (found) return found;
    }
  }
  return null;
}

function readEventDates(file) {
  const html = readFileSync(join(root, file), "utf8");
  const matches = [...html.matchAll(/<script type="application\/ld\+json">([\s\S]*?)<\/script>/g)];
  for (const match of matches) {
    let data;
    try {
      data = JSON.parse(match[1]);
    } catch {
      continue;
    }
    const eventNode = findEventNode(data);
    if (eventNode?.startDate) {
      const startDate = String(eventNode.startDate).slice(0, 10);
      const endDate = eventNode.endDate ? String(eventNode.endDate).slice(0, 10) : startDate;
      return { startDate, endDate };
    }
  }
  throw new Error(`No Event/MusicEvent startDate found in ${file}`);
}

// Exported for direct unit testing (see scripts/select-featured-event.test.mjs).
export function pickFeaturedEvent(events, todayIso) {
  const current = events
    .filter((event) => event.startDate <= todayIso && todayIso <= event.endDate)
    .sort((a, b) => a.startDate.localeCompare(b.startDate));
  if (current.length > 0) return current[0];

  const future = events
    .filter((event) => event.startDate > todayIso)
    .sort((a, b) => a.startDate.localeCompare(b.startDate));
  if (future.length > 0) return future[0];

  return null;
}

// Renders only the inner content of the "evento in evidenza" section (the
// .container.section-grid block). The outer <section> tag is emitted once
// by updateHub() and carries a stable id/data-event-id so the runtime
// script can swap this inner HTML in place without touching anything else
// on the page.
// A visually-hidden (but real, DOM-present) separator inserted between
// adjacent text values that would otherwise be extracted with zero
// whitespace between them (e.g. "2-5 luglioEdizione") by any text-extraction
// method that ignores CSS layout - textContent, naive tag-stripping,
// whitespace-collapsing readers, etc. Purely cosmetic for sighted users:
// the existing .visually-hidden utility class clips it out of view.
const TEXT_SEPARATOR = '<span class="visually-hidden"> — </span>';

function renderEventBlock(event) {
  const factsHtml = event.facts
    .map(([label, value]) => `<div><dt>${label}</dt><dd>${value}${TEXT_SEPARATOR}</dd></div>`)
    .join("");
  const teaserHtml = event.teaser.map((paragraph) => `<p>${paragraph}</p>`).join("");

  return `<div class="container section-grid">
          <article class="card content-card">
            <p class="eyebrow eyebrow--dark">In evidenza &middot; ${event.dateLabel}</p>
            <h2>${event.name}</h2>
            ${teaserHtml}
            <dl class="summary-list">${factsHtml}</dl>
            <p style="margin-top: 1rem;">
              <a class="button" href="${event.url}">Apri la scheda evento</a>
            </p>
          </article>

          <aside class="card event-feature__media">
            <picture>
              <source type="image/webp" srcset="${event.image.webp}">
              <img alt="${event.image.alt}" decoding="async" height="${event.image.height}" loading="lazy" src="${event.image.jpg}" width="${event.image.width}">
            </picture>
          </aside>
        </div>`;
}

function renderFallbackBlock() {
  return `<div class="container section-grid">
          <article class="card content-card">
            <p class="eyebrow eyebrow--dark">Prossimi aggiornamenti</p>
            <h2>Il calendario si aggiorna nel corso della stagione</h2>
            <p>
              In questo momento non c'&egrave; un evento imminente da mettere in evidenza. Il modo pi&ugrave; affidabile per
              orientarsi resta il calendario completo, che raccoglie tutti gli appuntamenti confermati.
            </p>
            <p style="margin-top: 1rem;">
              <a class="button" href="/calendario-eventi-montefiascone">Apri il calendario eventi</a>
            </p>
          </article>

          <aside class="card">
            <h2 class="card__eyebrow">Nel frattempo</h2>
            <ul class="plain-list">
              <li><a href="/eventi-estate-montefiascone-2026">Estate 2026 a Montefiascone</a></li>
              <li><a href="/calendario-eventi-montefiascone">Calendario eventi</a></li>
            </ul>
          </aside>
        </div>`;
}

export function buildFeaturedSection(todayIso) {
  const events = FEATURED_EVENT_CANDIDATES.map((event) => ({
    ...event,
    ...readEventDates(event.file),
  }));
  const winner = pickFeaturedEvent(events, todayIso);
  const eventId = winner ? winner.id : "fallback";
  const innerHtml = winner ? renderEventBlock(winner) : renderFallbackBlock();
  return { events, eventId, innerHtml };
}

function updateHub() {
  const todayIso = new Date().toISOString().slice(0, 10);
  const { events, eventId, innerHtml } = buildFeaturedSection(todayIso);

  // Manifest for the runtime reconciliation script: only ids and dates,
  // nothing editorial, so it stays tiny and cannot drift from the copy
  // baked into the alternates below.
  const manifest = events.map(({ id, startDate, endDate }) => ({ id, startDate, endDate }));

  // Pre-rendered alternates for every candidate plus the fallback, using
  // the exact same render functions as the visible build-time pick. The
  // runtime script only ever swaps to one of these - it never generates
  // markup itself.
  const alternates = events
    .map((event) => `<template data-event-id="${event.id}">${renderEventBlock(event)}</template>`)
    .join("\n            ");
  const fallbackTemplate = `<template data-event-id="fallback">${renderFallbackBlock()}</template>`;

  const section = `      <section class="section section--alt" id="evento-in-evidenza" data-event-id="${eventId}">
        ${innerHtml}
      </section>
      <div id="featured-event-alternates" hidden aria-hidden="true">
            ${alternates}
            ${fallbackTemplate}
      </div>
      <script type="application/json" id="featured-event-manifest">${JSON.stringify(manifest)}</script>
      <script src="/js/featured-event-runtime.js" defer></script>`;

  const hubPath = join(root, HUB_FILE);
  const html = readFileSync(hubPath, "utf8");
  const startIndex = html.indexOf(MARKER_START);
  const endIndex = html.indexOf(MARKER_END);
  if (startIndex === -1 || endIndex === -1) {
    throw new Error(`Featured event markers not found in ${HUB_FILE}`);
  }

  const updated =
    html.slice(0, startIndex + MARKER_START.length) +
    "\n" +
    section +
    "\n      " +
    html.slice(endIndex);

  writeFileSync(hubPath, updated, "utf8");
  console.log(`Featured event on ${HUB_FILE} refreshed for ${todayIso} (${eventId})`);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  updateHub();
}
